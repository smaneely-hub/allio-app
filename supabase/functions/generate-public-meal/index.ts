import { serve } from 'https://deno.land/std@0.224.0/http/server.ts'

const LLM_API_KEY = Deno.env.get('LLM_API_KEY') || ''
const LLM_ENDPOINT = Deno.env.get('LLM_ENDPOINT') || 'https://openrouter.ai/api/v1/chat/completions'
const LLM_MODEL = Deno.env.get('LLM_PUBLIC_MODEL') || Deno.env.get('LLM_MODEL') || 'google/gemini-2.5-flash'

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type, x-forwarded-for',
}

const ipRateLimit = new Map<string, number[]>()
const HOUR_MS = 60 * 60 * 1000
const MAX_REQUESTS_PER_HOUR = 5

function cleanupAndCheckRateLimit(ip: string) {
  const now = Date.now()
  const existing = ipRateLimit.get(ip) || []
  const fresh = existing.filter((timestamp) => now - timestamp < HOUR_MS)
  if (fresh.length >= MAX_REQUESTS_PER_HOUR) {
    ipRateLimit.set(ip, fresh)
    return false
  }
  fresh.push(now)
  ipRateLimit.set(ip, fresh)
  return true
}

function asArray(value: unknown) {
  return Array.isArray(value) ? value : []
}

function asString(value: unknown, fallback = '') {
  return typeof value === 'string' ? value : fallback
}

function asNumber(value: unknown, fallback = 0) {
  const parsed = Number(value)
  return Number.isFinite(parsed) ? parsed : fallback
}

function normalizeIngredientGroup(group: any) {
  return {
    label: typeof group?.label === 'string' && group.label.trim() ? group.label.trim() : null,
    ingredients: asArray(group?.ingredients).map((ingredient: any) => ({
      amount: asString(ingredient?.amount, ''),
      unit: asString(ingredient?.unit, ''),
      item: asString(ingredient?.item, ''),
      note: typeof ingredient?.note === 'string' ? ingredient.note : undefined,
      optional: Boolean(ingredient?.optional),
    })).filter((ingredient: any) => ingredient.item),
  }
}

function normalizeInstructionGroup(group: any) {
  return {
    label: typeof group?.label === 'string' && group.label.trim() ? group.label.trim() : null,
    steps: asArray(group?.steps).map((step: any) => ({
      text: typeof step === 'string' ? step : asString(step?.text, ''),
      tip: typeof step?.tip === 'string' ? step.tip : undefined,
    })).filter((step: any) => step.text),
  }
}

function normalizeRecipe(recipe: any) {
  const ingredientGroups = asArray(recipe?.ingredientGroups).map(normalizeIngredientGroup).filter((group: any) => group.ingredients.length > 0)
  const instructionGroups = asArray(recipe?.instructionGroups).map(normalizeInstructionGroup).filter((group: any) => group.steps.length > 0)

  return {
    title: asString(recipe?.title, 'Generated recipe'),
    slug: asString(recipe?.slug, ''),
    description: asString(recipe?.description, ''),
    yield: asString(recipe?.yield, recipe?.servings ? `${recipe.servings} servings` : ''),
    prepTime: asNumber(recipe?.prepTime, 0),
    cookTime: asNumber(recipe?.cookTime, 0),
    totalTime: asNumber(recipe?.totalTime, asNumber(recipe?.prepTime, 0) + asNumber(recipe?.cookTime, 0)),
    difficulty: ['easy', 'medium', 'advanced'].includes(recipe?.difficulty) ? recipe.difficulty : 'medium',
    servings: Math.max(1, asNumber(recipe?.servings, 2)),
    ingredientGroups,
    instructionGroups,
    tips: asArray(recipe?.tips).filter((tip) => typeof tip === 'string'),
    substitutions: asArray(recipe?.substitutions).map((sub: any) => ({
      original: asString(sub?.original, ''),
      substitute: asString(sub?.substitute, ''),
      note: typeof sub?.note === 'string' ? sub.note : undefined,
    })).filter((sub: any) => sub.original && sub.substitute),
    tags: {
      cuisine: asString(recipe?.tags?.cuisine, ''),
      mealType: asString(recipe?.tags?.mealType, 'dinner'),
      dietary: asArray(recipe?.tags?.dietary).filter((value) => typeof value === 'string'),
      season: typeof recipe?.tags?.season === 'string' ? recipe.tags.season : undefined,
      cookingMethod: asArray(recipe?.tags?.cookingMethod).filter((value) => typeof value === 'string'),
    },
    nutrition: recipe?.nutrition && typeof recipe.nutrition === 'object'
      ? {
          calories: asNumber(recipe.nutrition.calories, 0),
          protein: asString(recipe.nutrition.protein, ''),
          carbs: asString(recipe.nutrition.carbs, ''),
          fat: asString(recipe.nutrition.fat, ''),
          fiber: typeof recipe.nutrition.fiber === 'string' ? recipe.nutrition.fiber : undefined,
          sodium: typeof recipe.nutrition.sodium === 'string' ? recipe.nutrition.sodium : undefined,
        }
      : undefined,
    sourceNote: typeof recipe?.sourceNote === 'string' ? recipe.sourceNote : 'Generated by Allio public meal generator.',
    imagePrompt: typeof recipe?.imagePrompt === 'string' ? recipe.imagePrompt : undefined,
  }
}

function validateRecipe(recipe: any) {
  if (!recipe.title || !recipe.description) return 'Missing title or description'
  if (!recipe.ingredientGroups?.length) return 'Missing ingredient groups'
  if (!recipe.instructionGroups?.length) return 'Missing instruction groups'
  const ingredientCount = recipe.ingredientGroups.flatMap((group: any) => group.ingredients).length
  const stepCount = recipe.instructionGroups.flatMap((group: any) => group.steps).length
  if (ingredientCount < 5) return 'Too few ingredients'
  if (stepCount < 4) return 'Too few steps'
  return null
}

