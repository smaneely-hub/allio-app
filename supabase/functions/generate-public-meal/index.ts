async function callLlm(messages: Array<{ role: string; content: string }>) {
  const response = await fetch(LLM_ENDPOINT, {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${LLM_API_KEY}`,
      'Content-Type': 'application/json',
      'HTTP-Referer': 'https://allio.life',
      'X-Title': 'Allio Public Meal Generator',
    },
    body: JSON.stringify({
      model: LLM_MODEL,
      messages,
      response_format: { type: 'json_object' },
      temperature: 0.8,
    }),
  })

  if (!response.ok) {
    const text = await response.text()
    throw new Error(`OpenRouter error ${response.status}: ${text}`)
  }

  return await response.json()
}

function extractLlmContent(llm: any) {
  const content = llm?.choices?.[0]?.message?.content
  if (typeof content === 'string') return content
  if (Array.isArray(content)) {
    const text = content
      .map((part) => typeof part?.text === 'string' ? part.text : '')
      .join('')
      .trim()
    if (text) return text
  }
  return ''
}

async function generateRecipe(payload: any) {
  const systemPrompt = buildPrompt(payload)
  const baseMessages = [
    { role: 'system', content: systemPrompt },
    { role: 'user', content: 'Generate one recipe now.' },
  ]

  for (let attempt = 0; attempt < 2; attempt += 1) {
    const llm = await callLlm(baseMessages)
    const content = extractLlmContent(llm)
    if (!content) throw new Error('LLM returned empty content')

    try {
      const parsed = JSON.parse(content)
      const normalized = normalizeRecipe(parsed)
      const validationError = validateRecipe(normalized)
      if (!validationError) return normalized

      if (attempt === 0) {
        baseMessages.push({
          role: 'user',
          content: `Your previous response was malformed: ${validationError}. Return corrected JSON only, preserving all requested fields.`,
        })
        continue
      }
      throw new Error(validationError)
    } catch (error) {
      if (attempt === 0) {
        baseMessages.push({
          role: 'user',
          content: 'Your previous response was malformed JSON. Return corrected JSON only, with the exact requested schema.',
        })
        continue
      }
      throw error
    }
  }

  throw new Error('Unable to generate recipe')
}

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  if (req.method !== 'POST') {
    return new Response(JSON.stringify({ error: 'Method not allowed' }), {
      status: 405,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    })
  }

  const forwardedFor = req.headers.get('x-forwarded-for') || 'unknown'
  const ip = forwardedFor.split(',')[0].trim() || 'unknown'
  if (!cleanupAndCheckRateLimit(ip)) {
    return new Response(JSON.stringify({ error: 'Too many requests. Please wait a bit and try again.' }), {
      status: 429,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    })
  }

  try {
    if (!LLM_API_KEY) {
      return new Response(JSON.stringify({
        ok: false,
        error: 'Public meal generator is not configured yet. Missing LLM API key.',
      }), {
        status: 503,
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      })
    }

    const payload = await req.json()
    const recipe = await generateRecipe(payload)

    return new Response(JSON.stringify({ ok: true, recipe }), {
      status: 200,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    })
  } catch (error) {
    console.error('[generate-public-meal] error', error?.message || error)
    return new Response(JSON.stringify({
      ok: false,
      error: 'Sorry, I could not generate a polished recipe right now. Please try again in a moment.',
    }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    })
  }
})
